#include <bits/stdc++.h>
using namespace std;

int main() {
    int t;
    cin >> t;
    while (t--) {
        int n, m;
        long long x, y;
        cin >> n >> m >> x >> y;

        for (int i = 0; i < n; i++) {
            long long a;
            cin >> a;
        }

        for (int i = 0; i < m; i++) {
            long long b;
            cin >> b;
        }

        cout << (n + m) << endl;
    }
    return 0;
}
